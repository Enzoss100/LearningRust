fn main() {
    // Hello World, the traditional first print of any programming language
    println!("Hello, world!");

    // Testing String Variable
    let mut newVar = "Testing Strings";

    // Print String Variable
    println!("{newVar}");

    // Initializing 2 integer variables
    let newNum1: i32 = 1;
    let newNum2: i32 = 5;

    // Testing Conditional Statements
    if newNum1 < 5 {
        println!("{newNum2}");

    }
}
